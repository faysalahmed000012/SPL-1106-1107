#include <stdio.h>

int main(){
 
    float a,b,c,average; // initializing float a,b,c and average.
    scanf("%f",&a); 
    scanf("%f",&b); 
    scanf("%f",&c); 

    average = (a*2 + b*3 + c*5)/(2+3+5); // calculating weighted average

    printf("MEDIA = %.1f\n",average); 

    return 0;


    return 0;
}